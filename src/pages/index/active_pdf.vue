			<p>在中华人民共和国境外（包括香港、澳门、台湾地区）是指保险人认可的根据所在国家或地区法律规定合法成立、运营并符合以下标准的医疗机构：</p>
			<p>1、主要运营目的是向受伤者和患病者提供留院治疗和护理服务；</p>
			<p>2、在一名或若干名医生的指导下为病人治疗，其中最少有一名合法执业资格的驻院医生驻诊；</p>
			<p>3、维持足够妥善的设备为病人提供医学诊断和治疗，并于机构内或由其管理的地方提供进行各种手术的设备；</p>
			<p>4、有合法执业的护士提供和指导二十四小时的全职护理服务。</p>
			<p><span>但不包括以下或类似的医疗机构：</span></p>
