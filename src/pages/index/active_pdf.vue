			<p class="title">中银保险有限公司<br> 航空旅客人身意外伤害保险（一年期）条款
			</p>
			<p class="title T_2">总则</p>
			<p><span>第一条</span> 本保险合同（以下简称“本合同”）由保险单和其他保险凭证及所附条款、投保单及与本合同有关的投保文件、合法有效的声明、批注、附贴批单、其他书面文件构成。凡涉及本合同的约定，均应采用书面形式。</p>
			<p><span>第二条</span> 除另有约定外，凡年满三周岁（含）至七十周岁（含），身体健康、能正常工作或正常生活的自然人可作为本合同的被保险人。</p>
			<p><span>第三条</span> 具有完全民事行为能力的被保险人本人、对被保险人有保险利益的其他人可作为投保人。投保人为他人投保的，必须征得被保险人的同意且确认保险金额（投保人为其未成年子女投保的，不受此限）。</p>
			<p><span>第四条</span> 订立本合同时，被保险人或投保人可指定一人或数人为身故保险金受益人。身故保险金受益人为数人时，应确定其受益顺序和受益份额；未确定受益份额的，各身故保险金受益人按照相等份额享有受益权。本合同的残疾保险金的受益人为被保险人本人。
			</p>
			<p>被保险人或投保人可以变更保险金受益人，但需书面通知保险人，由保险人在本合同上批注。对因保险金受益人变更发生的法律纠纷，保险人不承担任何责任。</p>
			<p>投保人指定或变更保险金受益人的，应经被保险人书面同意。被保险人为无民事行为能力人或限制民事行为能力人的，应由其监护人指定或变更保险金受益人。</p>
			<p>受益人故意造成被保险人身故、伤残的，或者故意杀害被保险人未遂的，该受益人丧失受益权。</p>
			<div style="font-weight: bold;color: #000;">
				<p class="title T_2">保险责任</p>
				<p><span>第五条</span> 在保险期间内被保险人持有效客票以乘客身份乘坐从事合法客运的飞机时遭受意外伤害事故，并因该意外伤害导致其身故、残疾的，保险人依照下列约定给付保险金。</p>
				<p>（一）身故保险责任</p>
				<p>在保险期间内被保险人遭受意外伤害事故，并自该事故发生之日起一百八十日内因该意外伤害为直接原因身故的，保险人按意外伤害身故保险金额给付身故保险金，对该被保险人的保险责任终止。</p>
				<p>
					被保险人因遭受意外伤害事故且自该事故发生日起下落不明，后经人民法院宣告死亡的，保险人按意外伤害身故保险金额给付身故保险金。但若被保险人被宣告死亡后生还的，保险金受领人应于知道或应当知道被保险人生还后三十日内退还保险人给付的身故保险金。</p>
				<p>（二）残疾保险责任</p>
				<p>
					在保险期间内被保险人遭受意外伤害事故，并自该事故发生之日起一百八十日内因该意外伤害为直接原因造成本合同所附《人身保险伤残评定标准（行业标准）》（简称《评定标准》）中列明的伤残程度之一的，保险人按《评定标准》中所对应的保险金给付比例乘以意外伤害残疾保险金额给付残疾保险金。如第一百八十日治疗仍未结束的，按当日的身体情况进行伤残鉴定，并据此给付残疾保险金。</p>
				<p>被保险人如在遭受本次意外伤害事故之前已有残疾的，保险人按合并后的伤残程度在《评定标准》中所对应的保险金给付比例乘以意外伤害残疾保险金额给付残疾保险金，<span>但应扣减原有伤残程度在《评定标准》中所对应的残疾保险金（即所对应的保险金给付比例与意外伤害残疾保险金额的乘积）</span>。
				</p>
				<p>保险人对每一被保险人的残疾保险金给付总额以保险单载明的该被保险人的意外伤害残疾保险金额为限。一次或累计给付同一被保险人的残疾保险金达到该被保险人的意外伤害残疾保险金额时，保险人对该被保险人的残疾保险责任终止。</p>
				<p>（三）被保险人乘坐民航客机，保险人所承担保险责任期间起讫的界定</p>
				<p>从每次被保险人踏入民航班机的舱门起至抵达目的地走出民航班机的舱门止。<span>但被保险人在进入飞机舱门前或离开飞机舱门后遭受意外伤害事故导致身故或残疾的，保险人不承担给付保险金责任。</span></p>
				<p class="title T_2">责任免除</p>
				<p><span>第六条</span> <span>被保险人因下列原因而导致身故或残疾的，保险人不承担给付保险金责任：</span></p>
				<p><span>（一）投保人的故意行为；</span></p>
				<p><span>（二）被保险人故意自伤或自杀，但被保险人自杀时为无民事行为能力人的除外；</span></p>
				<p><span>（三）因被保险人挑衅或故意行为而导致的打斗、被袭击或被谋杀；</span></p>
				<p><span>（四）被保险人妊娠（含宫外孕）、流产、分娩（含剖宫产）、药物过敏、食物中毒、高原反应、中暑、猝死；</span></p>
				<p><span>（五）被保险人未遵医嘱服用、涂用、注射药物；</span></p>
				<p><span>（六）被保险人受酒精、毒品、管制药物的影响；</span></p>
				<p><span>（七）疾病；</span></p>
				<p><span>（八）非因意外伤害导致的细菌或病毒感染；</span></p>
				<p><span>（九）任何生物、化学、原子能武器，原子能或核能装置所造成的爆炸、灼伤、污染或辐射；</span></p>
				<p><span>（十）被保险人因遭受意外伤害以外的原因失踪而被法院宣告死亡；</span></p>
				<p><span>（十一）恐怖袭击；</span></p>
				<p><span>（十二）被保险人在机舱内从事危害公共安全的活动，被机上安全人员、乘务人员或乘客采用暴力制止。</span></p>
				<p><span>第七条</span> <span>被保险人在下列期间遭受意外伤害导致身故或残疾的，保险人不承担给付保险金责任</span>：</p>
				<p><span>（一）战争、军事行动、武装叛乱或暴乱期间；</span></p>
				<p><span>（二）被保险人从事违法、犯罪活动期间或被依法拘留、服刑、在逃期间；</span></p>
				<p><span>（三）被保险人存在精神和行为障碍（以世界卫生组织颁布的《疾病和有关健康问题的国际统计分类（ICD-10）》为准）期间。</span></p>
			</div>
			<p class="title T_2">保险金额</p>
			<p><span>第八条</span> 每一被保险人的分项保险金额是保险人承担给付该被保险人该项保险金责任的最高限额。</p>
			<p>每一被保险人的意外伤害身故保险金额、意外伤害残疾保险金额由投保人、保险人双方约定，并在保险单中载明。若被保险人为未成年人，保险金额须符合国务院保险监督管理机构的相关规定。</p>
			<p class="title T_2">保险期间</p>
			<p><span>第九条</span> 除另有约定外，保险期间为一年，以保险单载明的起讫时间为准。</p>
			<p class="title T_2">保险人义务</p>
			<p><span>第十条</span> 订立本合同时，保险人应当向投保人说明本合同的内容。</p>
			<p>对保险合同中免除保险人责任的条款，保险人在订立合同时应当在投保单、保险单或者其他保险凭证上作出足以引起投保人注意的提示，并对该条款的内容以书面或者口头形式向投保人作出明确说明，未作提示或者明确说明的，该条款不产生效力。</p>
			<p><span>第十一条</span> 本合同成立后，保险人应当及时向投保人签发保险单或其他保险凭证。</p>
			<p>在本合同有效期内，投保人提出书面批改申请的，保险人审核同意后，应及时在保险单或者其他保险凭证上批注或者附贴批单。 </p>
			<p><span>第十二条</span> 保险人依据本保险条款第十七条和第十八条所取得的保险合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。发生保险事故的，保险人承担给付保险金责任。自本合同成立之日起超过二年的，保险人不得解除合同；发生保险事故的，保险人承担给付保险金责任。
			</p>
			<p>保险人在本合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。</p>
			<p><span>第十三条</span> 保险人按照本合同的约定，认为投保人、被保险人或受益人提供的有关索赔证明和资料不完整的，应当及时一次性通知投保人、被保险人或受益人补充提供。</p>
			<p><span>第十四条</span> 保险人收到保险金申请人的给付保险金请求后，应当及时作出是否属于保险责任的核定；情形复杂的，应当在三十日内作出核定，但本合同另有约定的除外。</p>
			<p>保险人应当将核定结果通知保险金申请人；对属于保险责任的，在与保险金申请人达成给付保险金的协议后十日内，履行给付保险金义务。本合同对给付保险金的期限有约定的，保险人应当按照约定履行给付保险金的义务。</p>
			<p>保险人依照前款的规定作出核定后，对不属于保险责任的，应当自作出核定之日起三日内向保险金申请人发出拒绝给付保险金通知书，并说明理由。</p>
			<p><span>第十五条</span> 保险人自收到给付保险金的请求和有关证明、资料之日起六十日内，对其给付的数额不能确定的，应当根据已有证明和资料可以确定的数额先予支付；保险人最终确定给付的数额后，应当支付相应的差额。</p>
			<p class="title T_2">投保人、被保险人义务</p>
			<p><span>第十六条</span> 除另有约定外，投保人应在保险责任起始日前一次性交清保险费，<span>保险费交清前发生的保险事故，保险人按投保人已缴纳保险费占应缴纳保险费的比例承担给付保险金责任。 </span></p>
			<p><span>第十七条</span> 投保人申请投保时，应按被保险人的周岁年龄填写。</p>
			<p><span>投保人申报的被保险人年龄不真实，并且其真实年龄不符合本合同约定的年龄限制的，保险人有权解除本合同，并向投保人退还现金价值。 </span></p>
			<p><span>第十八条</span> 投保人应如实填写投保单并回答保险人提出的询问，履行如实告知义务。</p>
			<p><span>投保人故意或者因重大过失未履行前款规定的如实告知义务，足以影响保险人决定是否同意承保或者提高保险费率的，保险人有权解除本合同。</span></p>
			<p><span>投保人故意不履行如实告知义务的，保险人对于本合同解除前发生的保险事故，不承担给付保险金的责任，并不退还保险费。</span></p>
			<p><span>投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于本合同解除前发生的保险事故，不承担给付保险金的责任，但应当退还保险费。</span></p>
			<p><span>第二十一条</span> 投保人住所或通讯地址变更时，应及时以书面形式通知保险人。投保人未通知的，保险人按本合同所载的最后住所或通讯地址发送的有关通知，均视为已送达投保人。</p>
			<p><span>第二十二条</span> 在保险期间内，投保人需变更本合同其他内容的，应以书面形式向保险人提出申请。保险人同意后出具批单，并在本合同中批注。</p>
			<p>若被保险人身故，则保险人不接受本合同中有关该被保险人的任何内容的变更申请。</p>
			<p><span>第二十三条</span> 发生保险责任范围内的事故后，投保人、被保险人或受益人应及时通知保险人，并书面说明事故发生的原因、经过和损失情况；故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担给付保险金的责任，但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外。
			</p>
			<p>上述约定，不包括因不可抗力而导致的迟延。</p>
			<p class="title T_2">保险金申请与给付</p>
			<p><span>第二十四条</span> 保险金申请人向保险人申请给付保险金时，应填写保险金给付申请书，并提交以下材料。保险金申请人因特殊原因不能提供以下材料的，应提供其它合法有效的材料。若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件。
				<span>保险金申请人未能提供有关材料，导致保险人难以确定保险事故的性质、原因、损失程度等的，保险人对无法确定的部分不承担给付保险金的责任。</span>
			</p>
			<p>（一）身故保险金申请</p>
			<p>1、保险金给付申请书；</p>
			<p>2、保险单或其他保险凭证原件；</p>
			<p>3、保险金申请人的身份证明；</p>
			<p>4、公安部门或司法部门、保险人认可的医疗机构出具的被保险人死亡证明或验尸报告。若被保险人为宣告死亡，保险金申请人应提供法院出具的宣告死亡证明文件；</p>
			<p>5、被保险人的户籍注销证明；</p>
			<p>6、由承运人出具的意外伤害事故证明；</p>
			<p>7、保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料。</p>
			<p>（二）残疾保险金申请</p>
			<p>1、保险金给付申请书；</p>
			<p>2、保险单或其他保险凭证原件；</p>
			<p>3、保险金申请人的身份证明；</p>
			<p>4、保险人认可的伤残鉴定机构出具的伤残程度鉴定书；</p>
			<p>5、由承运人出具的意外伤害事故证明；</p>
			<p>6、保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料。</p>
			<p><span>第二十五条</span> 保险金申请人向保险人请求给付保险金的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。</p>
			<p class="title T_2">合同的解除和争议处理</p>
			<p><span>第二十六条</span> 在本合同成立后，投保人可以书面形式通知保险人解除合同，但保险人已根据本合同约定给付保险金的除外。</p>
			<p>投保人解除本合同时，应填写保险合同解除申请书，并提供下列证明文件和资料：</p>
			<p>（一）保险合同解除申请书；</p>
			<p>（二）保险单或其他保险凭证原件；</p>
			<p>（三）保险费交付凭证；</p>
			<p>（四）投保人身份证明。</p>
			<p><span>投保人要求解除本合同，自保险人接到保险合同解除申请书之时起，本合同的效力终止。</span>保险人收到上述证明文件和资料之日起三十日内退还保险单的现金价值。</p>
			<p><span>第二十七条</span> 因履行本合同发生的争议，由当事人协商解决。协商不成的，提交保险单载明的仲裁机构仲裁；保险单未载明仲裁机构且争议发生后未达成仲裁协议的，依法向人民法院起诉。</p>
			<p><span>第二十八条</span> 与本合同有关的以及履行本合同产生的一切争议处理适用中华人民共和国法律（不包括香港、澳门及台湾法律）。</p>
			<p class="title T_2">释义</p>
			<p> 第二十九条 本合同涉及下列术语时，适用下列释义：</p>
			<p>（一） 周岁：以法定身份证明文件中记载的出生日期为基础计算的实足年龄。</p>
			<p>（二）保险人：指与投保人签订本合同的中银保险有限公司各分支机构。</p>
			<p>（三） 意外伤害：指以外来的、突发的、非本意的、非疾病的客观事件为直接且单独的原因致使被保险人身体受到的伤害。</p>
			<p>（四） 猝死：外表看似健康的人由于潜在的疾病或者功能障碍所引起的突然的出乎意料的死亡，猝死的认定以保险人认可的医疗机构的诊断和公安部门的鉴定为准。</p>
			<p>（五）现金价值：除另有约定外，现金价值=保险费×[1-(保险单已经过天数/保险期间天数)] ×75%。经过天数不足一天的按一天计算。</p>
			<p>（六）保险金申请人：身故保险金申请人是指受益人或被保险人的继承人或依法享有保险金请求权的其他自然人；残疾保险金申请人是指被保险人本人。</p>
			<p>（七）不可抗力：指不能预见、不能避免并不能克服的客观情况。</p>
